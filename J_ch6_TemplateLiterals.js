// In this we will discuss a new method of printing something.
    let name = "Ishant Sharma";
    let age = 18;


// normal ways of printing
    console.log ('name is',name,'and age is',age,'.');
    console.log ("name is "+name+" and age is "+age+".");    //java


//------------------------------------Back tick method---------------------------------------------------------------
    console.log(`name is ${name} and age is ${age}`);
// This method is very useful and easy in javascript used in many things.

// If we want to add " in our string then we can add it by using back tick method.
    console.log(`The hero of the movie said "I am steve". `);

// alternate ways of doing that.
    console.log('The hero of the movie said "I am steve". ');
    console.log("The hero of the movie said \"I am steve\". ");      //using the back slash
    
    console.log("The hero of the movie said 'I am steve'. ");      //for printing '
    console.log(`The hero of the movie said 'I am steve'. `);      //for printing '

    console.log("The hero of the movie said `I am steve`. ");      //for printing the backtick `
    console.log('The hero of the movie said `I am steve`. ');      //for printing the backtick `



